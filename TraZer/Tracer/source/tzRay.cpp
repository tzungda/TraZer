#include "tzRay.h"

// ================================================================================
tzRay::~tzRay(void) 
{
}

// ================================================================================
tzRay::tzRay(void)
	: o(0.0),
	d(0.0, 0.0, 1.0)
{}

// ================================================================================
tzRay::tzRay(const tzPoint3D& origin, const tzVector3D& dir)
	: o(origin), d(dir)
{}

// ================================================================================
tzRay::tzRay(const tzRay& ray)
	: o(ray.o),
	d(ray.d)
{}

// ================================================================================
tzRay&
tzRay::operator= (const tzRay& rhs) {

	if (this == &rhs)
		return (*this);

	o = rhs.o;
	d = rhs.d;

	return (*this);
}

